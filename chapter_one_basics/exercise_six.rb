# Write a program that calculates the square of 3 float numbers and outputs the result.
# Square root of a number is that number multiplied by itself.
puts 2.2 * 2.2
puts 3.3 * 3.3
puts 4.4 * 4.4

# A second way of writing this can be done using the exponentiation operator.

puts 2.2 ** 2
puts 3.3 ** 2
puts 4.4 ** 2