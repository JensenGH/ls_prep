# Write a program that will output the factorial of numbers 5, 6, 7, and 8.
# Assigned each to a variable, then output the result using those variables.

# The factorial function says to multiply all whole numbers from our chosen number down to 1.
# symbol: (!)
a = 5 * 4 * 3 * 2 * 1 # chosen number 5!
b = 6 * 5 * 4 * 3 * 2 * 1 # chosen number 6!
c = 7 * 6 * 5 * 4 * 3 * 2 * 1 # chosen number 7!
d = 8 * 7 * 6 * 5 * 4 * 3 * 2 * 1 # chosen number 8!

puts a
puts b
puts c
puts d